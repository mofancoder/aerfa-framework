CREATE TABLE `USER` (
  `GUID` varchar(36) NOT NULL,
  `USER_NAME` varchar(50) NOT NULL,
  `ACCOUNT` varchar(20) NOT NULL,
  `PASSWORD` varchar(200) NOT NULL,
  `DEPT_GUID` varchar(36) DEFAULT NULL,
  `DEPT_NAME` varchar(50) DEFAULT NULL,
  `ID_TYPE` decimal(1,0) NOT NULL,
  `ID_NUM` varchar(20) NOT NULL,
  `PHONE_NUM` varchar(11) DEFAULT NULL,
  `TEL_NUM` varchar(20) DEFAULT NULL,
  `EMAIL` varchar(50) DEFAULT NULL,
  `IS_VALIDATE` decimal(1,0) NOT NULL DEFAULT '1',
  `CHANGE_STATUS_REASON` varchar(200) DEFAULT NULL,
  `IS_LOCKED` decimal(1,0) NOT NULL DEFAULT '0',
  `LOCKED_TIME` decimal(19,0) DEFAULT NULL,
  `SORT_NUM` decimal(19,0) NOT NULL DEFAULT '0',
  `DESCRIPTION` varchar(200) DEFAULT NULL,
  `CREATE_TIME` decimal(19,0) DEFAULT NULL,
  `CREATOR_GUID` varchar(36) DEFAULT NULL,
  `CREATOR_NAME` varchar(50) DEFAULT NULL,
  `MODIFY_TIME` varchar(19) DEFAULT NULL,
  `MODIFIER_GUID` varchar(32) DEFAULT NULL,
  `MODIFIER_NAME` varchar(50) DEFAULT NULL,
  `IS_DELETED` decimal(1,0) DEFAULT '0',
  PRIMARY KEY (`GUID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;